IF NOT EXISTS (
    SELECT 1
    FROM  GOT_CURRENCY
    WHERE GOT_CURRENCY_PROGID = 'BTC'
)
BEGIN
    INSERT INTO GOT_CURRENCY (
        GOT_CURRENCY_NAME
       ,GOT_CURRENCY_PROGID
       ,GOT_CURRENCY_SYMBOL
       ,GOT_CURRENCY_TYPE_ID
       ,GOT_CURRENCY_ISACTIVE
    )
    SELECT 'Bitcoin'
          ,'BTC'
          ,'BTC'
          ,(SELECT GOT_CURRENCY_TYPE.GOT_CURRENCY_TYPE_ID
            FROM GOT_CURRENCY_TYPE
			WHERE GOT_CURRENCY_TYPE.GOT_CURRENCY_TYPE_PROGID = 'CRYPTO')
          ,1
END
GO

